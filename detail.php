                    Mastodon's foreboding contribution features interplaying acoustic guitars and funereal drums as the band sings, "Hide away your children / Then hide away yourself / Dig deep into mountain / Dig far beyond the sky / They come down / They come down the Wall" in honor of Game of Thrones' neighbors to the North. The track keeps building until the three-minute-mark, when&nbsp;the band&nbsp;unleashes a monster guitar solo.
                    </p>
                    
                    <p>
                    Catch the Throne: The Mixtape, Vol. 2 is due out March 17th and will feature new tracks by Method Man, Killswitch Engage, Ty Dolla $ign, Melanie Fiona, Estelle, Anthrax and Snoop Dogg, who said of the HBO series in a statement, "Off the top, I love Game of Thrones - I even gave my expert analysis of this season on my GGN show. It's like the rap game, it's gangster, about family, loyalty and trying to get to the top. I had to get involved. Give the fans a little bit of how I see it."
                    </p>
                    
                    <p>
                    In addition to crafting new music for Game of Thrones, Mastodon's Brent Hinds, Tool drummer Danny Carey and Off! guitarist Dimitri Coats have teamed up to form the new side project Legend of the Seagullmen. Mastodon's foreboding contribution features interplaying acoustic guitars and funereal drums as the band sings, "Hide away your children / Then hide away yourself / Dig deep into mountain / Dig far beyond the sky / They come down / They come down the Wall" in honor of Game of Thrones' neighbors to the North. The track keeps building until the three-minute-mark, when&nbsp;the band&nbsp;unleashes a monster guitar solo.
