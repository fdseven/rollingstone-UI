                    <a href="detail_portrait.php">DETAIL PORTRAIT</a> 
                    Winter is almost coming to Westeros as the fifth season of <em>Game of Thrones</em> draws near, but before George R.R. Martin's fantasy series <a href="http://www.rollingstone.com/tv/videos/game-of-thrones-season-five-trailer-bring-on-the-dragons-20150309">returns on April 12th</a>, HBO will drop the second volume of their <em><a href="http://www.rollingstone.com/tv/news/game-of-thrones-mixtape-recruits-snoop-dogg-method-man-anthrax-20150211">Catch the Throne: The Mixtape</a></em>, a mix of hip-hop and heavy metal tracks inspired by the show. The first cut off the compilation was Talib Kweli's "Lord of the Light," and today Mastodon premiered their <a target="_blank" href="http://pitchfork.com/news/58780-mastodon-share-white-walker-from-the-game-of-thrones-mixtape-catch-the-throne-vol-2/"><em>Throne</em> track "White Walker,"</a> an ode to the terrifying frostbitten undead that sulk beyond the Wall.
